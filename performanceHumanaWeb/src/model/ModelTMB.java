package model;

public class ModelTMB {

	private int codTmb;
    private int codAvaliacao;
    private String protocoloTmb;
    private double tmb;

    public ModelTMB() {
    }
    
    public int getCodTmb() {
        return codTmb;
    }

    public void setCodTmb(int codTmb) {
        this.codTmb = codTmb;
    }

    public int getCodAvaliacao() {
        return codAvaliacao;
    }

    public void setCodAvaliacao(int codAvaliacao) {
        this.codAvaliacao = codAvaliacao;
    }

    public String getProtocoloTmb() {
        return protocoloTmb;
    }

    public void setProtocoloTmb(String protocoloTmb) {
        this.protocoloTmb = protocoloTmb;
    }

    public double getTmb() {
        return tmb;
    }

    public void setTmb(double tmb) {
        this.tmb = tmb;
    }
    
    
}
